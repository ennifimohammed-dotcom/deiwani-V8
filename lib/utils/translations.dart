class CurrencyInfo {
  final String code;
  final String nameAr;
  final String nameFr;
  final String nameEn;
  final String symbol;
  final String flag;

  const CurrencyInfo({
    required this.code,
    required this.nameAr,
    required this.nameFr,
    required this.nameEn,
    required this.symbol,
    required this.flag,
  });

  String displayName(String lang) {
    switch (lang) {
      case 'fr': return nameFr;
      case 'en': return nameEn;
      default: return nameAr;
    }
  }
}

